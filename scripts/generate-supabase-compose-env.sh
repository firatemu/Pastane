#!/usr/bin/env bash
# Build a Docker Compose env file for the official Supabase stack from Pastane .env.production.
# Maps SUPABASE_JWT_SECRET → JWT_SECRET so Pastane API JWT_SECRET is never overwritten.
#
# Usage:
#   bash scripts/generate-supabase-compose-env.sh [.env.production] [output-file]
# Default output: docker/supabase/.runtime.env (gitignored)
set -euo pipefail

ROOT="$(cd "$(dirname "$0")/.." && pwd)"
ENV_FILE="${1:-$ROOT/.env.production}"
OUT="${2:-$ROOT/docker/supabase/.runtime.env}"

if [[ ! -f "$ENV_FILE" ]]; then
  echo "generate-supabase-compose-env: missing $ENV_FILE" >&2
  exit 1
fi

get_kv() {
  grep -E "^[[:space:]]*${1}=" "$ENV_FILE" 2>/dev/null | tail -n1 | cut -d= -f2- | sed 's/^"//;s/"$//;s/^'"'"'//;s/'"'"'$//' || true
}

require() {
  local v
  v="$(get_kv "$1")"
  if [[ -z "${v// }" ]]; then
    echo "generate-supabase-compose-env: missing $1 in $ENV_FILE" >&2
    exit 1
  fi
  printf '%s' "$v"
}

POSTGRES_PASSWORD="$(require POSTGRES_PASSWORD)"
SUPABASE_JWT_SECRET="$(require SUPABASE_JWT_SECRET)"
DASHBOARD_USERNAME="$(require DASHBOARD_USERNAME)"
DASHBOARD_PASSWORD="$(require DASHBOARD_PASSWORD)"
ANON_KEY="$(require SUPABASE_ANON_KEY)"
SERVICE_ROLE_KEY="$(require SUPABASE_SERVICE_ROLE_KEY)"
SECRET_KEY_BASE="$(require SUPABASE_SECRET_KEY_BASE)"
VAULT_ENC_KEY="$(require SUPABASE_VAULT_ENC_KEY)"
PG_META_CRYPTO_KEY="$(require SUPABASE_PG_META_CRYPTO_KEY)"
LOGFLARE_PUBLIC="$(require SUPABASE_LOGFLARE_PUBLIC_TOKEN)"
LOGFLARE_PRIVATE="$(require SUPABASE_LOGFLARE_PRIVATE_TOKEN)"
SUPABASE_PUBLIC_URL="$(require SUPABASE_PUBLIC_URL)"

# Optional with defaults
POOLER_TENANT_ID="$(get_kv SUPABASE_POOLER_TENANT_ID)"
POOLER_TENANT_ID="${POOLER_TENANT_ID:-pastane-prod}"
STUDIO_ORG="$(get_kv SUPABASE_STUDIO_ORG)"
STUDIO_ORG="${STUDIO_ORG:-Pastane}"
STUDIO_PROJECT="$(get_kv SUPABASE_STUDIO_PROJECT)"
STUDIO_PROJECT="${STUDIO_PROJECT:-Production}"

mkdir -p "$(dirname "$OUT")"
cat > "$OUT" <<EOF
